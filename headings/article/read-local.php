        <h1>Le premier patient guéri appelle la population à la responsabilité</h1>
        <p class="source-date"><a href="https://www.gabonreview.com/covid-19-gabon-le-premier-patient-gueri-appelle-la-population-a-la-responsabilite/">Sources :</a> Gabonreview - hier à 00:31</p>
        <img src="https://lepetitjournal.com/sites/default/files/styles/main_article/public/2020-04/Happy.jpg?itok=BXINbOPV" alt="Le premier patient guéri appelle la population à la responsabilité">
        <div class="texte">
            <span id="docs-internal-guid-3e54ac72-7fff-f3dc-34e5-6e2667346435">
                <p dir="ltr" style="line-height: 1.8; margin-right: 15pt; margin-top: 0pt; margin-bottom: 0pt; padding: 0pt 0pt 19pt;"><span style="font-size: 12pt; font-family: Arial; color: rgb(102, 102, 102); background-color: transparent; font-style: italic; font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; white-space: pre-wrap;">Guéri du Covid-19 après 21 jours de traitement à l’hôpital des instructions des armées Omar Bongo, le 1er patient déclaré malade du Covid-19 au Gabon veut sensibiliser sur la pandémie.</span></p>
                <p dir="ltr" style="line-height: 1.8; margin-right: 15pt; margin-top: 0pt; margin-bottom: 0pt; padding: 0pt 0pt 19pt;">&nbsp;</p>
                <p dir="ltr" style="line-height: 1.8; margin-right: 15pt; margin-top: 0pt; margin-bottom: 0pt; padding: 0pt 0pt 19pt;"><span style="font-size: 12pt; font-family: Arial; color: rgb(102, 102, 102); background-color: transparent; font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; white-space: pre-wrap;">Cinq jours après sa sortie de l’hôpital des instructions des armées Omar Bongo où il été traité pendant 21 jours du Coronavirus, le premier cas déclaré au Gabon souhaite sensibiliser la population sur cette pandémie. Dans une vidéo postée sur Facebook le 8 avril, il invite les populations à adopter un comportement responsable en respectant les gestes barrières, car a-t-il averti, </span><span style="font-size: 12pt; font-family: Arial; color: rgb(102, 102, 102); background-color: transparent; font-style: italic; font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; white-space: pre-wrap;">«l’ennemi est invisible».</span></p>
                <p dir="ltr" style="line-height: 1.8; margin-right: 15pt; margin-top: 0pt; margin-bottom: 0pt; padding: 0pt 0pt 19pt;"><span style="font-size: 12pt; font-family: Arial; color: rgb(102, 102, 102); background-color: transparent; font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; white-space: pre-wrap;">Dans cette vidéo où il s’exprime à visage découvert, le jeune patient de 27 ans n’a pas manqué de manifester sa gratitude envers tous ceux qui l’ont soutenu dans cette épreuve difficile</span><span style="font-size: 12pt; font-family: Arial; color: rgb(102, 102, 102); background-color: transparent; font-style: italic; font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; white-space: pre-wrap;">. «Comme vous le savez, j’ai été déclaré positif au Covid-19. J’ai été placé en quarantaine à l’hôpital d’instructions des armées. Après 26 jours sur un lit d’hôpital, j’en suis totalement rétabli. Je vous remercie pour tous vos messages de soutien et d’encouragement. Vous m’avez aidé à traverser cette période ô combien singulière pour moi»,</span><span style="font-size: 12pt; font-family: Arial; color: rgb(102, 102, 102); background-color: transparent; font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; white-space: pre-wrap;"> a-t-il dit.</span></p>
                <p dir="ltr" style="line-height: 1.8; margin-right: 15pt; margin-top: 0pt; margin-bottom: 0pt; padding: 0pt 0pt 19pt;"><span style="font-size: 12pt; font-family: Arial; color: rgb(102, 102, 102); background-color: transparent; font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; white-space: pre-wrap;">Pour le jeune compatriote, sa sortie de l’hôpital marque la 1ère bataille remportée dans la lutte contre la Covid-19 : </span><span style="font-size: 12pt; font-family: Arial; color: rgb(102, 102, 102); background-color: transparent; font-style: italic; font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; white-space: pre-wrap;">«aujourd’hui nous avons gagné une bataille, mais la guerre est loin d’être terminée»,</span><span style="font-size: 12pt; font-family: Arial; color: rgb(102, 102, 102); background-color: transparent; font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; white-space: pre-wrap;"> a-t-il lancé. Il a invité les populations au respect scrupuleux des gestes barrières. </span><span style="font-size: 12pt; font-family: Arial; color: rgb(102, 102, 102); background-color: transparent; font-style: italic; font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; white-space: pre-wrap;">«Ayons des comportements responsables. Respectons les gestes barrières, car, l’ennemi est invisible»,</span><span style="font-size: 12pt; font-family: Arial; color: rgb(102, 102, 102); background-color: transparent; font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; white-space: pre-wrap;"> a-t-il averti.</span></p>
                <p dir="ltr" style="line-height: 1.8; margin-right: 15pt; margin-top: 0pt; margin-bottom: 19pt;"><span style="font-size: 12pt; font-family: Arial; color: rgb(102, 102, 102); background-color: transparent; font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; white-space: pre-wrap;">Employé Gabon Oil Company, il avait contracté la maladie lors de son séjour dans la ville de Bordeaux en France. Thermo flashé dès sa descente d’avion à l’Aéroport international Léon Mba, il n’avait pourtant présenté aucune anomalie sanitaire. 5 jours plus tard, il avait été testé positif au Covid-19 le 12 mars. Sitôt, il avait été conduit puis interné à l’hôpital d’instruction des armées Omar Bongo pour suivre des soins qui lui ont permis de recouvrer la guérison après 21 jours de traitement.</span></p>
