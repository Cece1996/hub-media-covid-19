Avec Marseille, Diouf a presque toujours entretenu une relation charnelle et passionnée. Son père Demba, qui fût notamment mécanicien miliaire pour l’armée française au Tchad, le pays qui a vu naître le futur dirigeant, avait décidé de l’envoyer dans le sud de la France à 18 ans, après un parcours scolaire parfois erratique  à Dakar, où la famille était revenue s’installer en juin 1952. Il rêvait de voir son fils accomplir une carrière de militaire.

Mais à l’uniforme, Diouf préfère une autre vie. Son père ne l’accepte pas, et le jeune homme multiplie les petits boulots pour subsister, jusqu’au jour où il intègre le quotidien La Marseillaise en tant que pigiste. Là, il gravit rapidement les échelons et devient chef de la rubrique football, en charge de la couverture de l’OM, avant de connaître deux autres expériences moins heureuses dans la profession, à l’Hebdomadaire d’abord, au Sport ensuite.


Cultivé, courtois et sans langue de bois

Mais pendant toutes ces années passées à arpenter les stades français et européens, Pape Diouf remplit son carnet d’adresses et tisse des liens amicaux avec de nombreux joueurs. Une fois l’aventure de la presse achevée, il organise pour plusieurs footballeurs des jubilés en Afrique, avant de se lancer, un peu par hasard, dans le métier d’agent, une profession escortée d’une réputation sulfureuse.


Joseph-Antoine Bell, le gardien camerounais, et Basile Boli, l’international français d’origine ivoirienne, lui demandent de défendre leurs intérêts. Pape Diouf accepte et créé Mondial Promotion en 1989, avant de devenir rapidement l’agent de nombreux footballeurs (Desailly, Drogba, Lama, Gallas, Song, Pirès, Coupet, Omam-Biyik, Blanc, Nasri…), avec qui il gardera toujours de bonnes relations. Diouf laissera auprès des dirigeants des clubs français et européens, avec qui il passera, parfois, de longues heures à négocier les contrats des joueurs, l’image d’un homme courtois, cultivé, attentif, qui en imposait par sa taille et son charisme, et réputé dur en affaires.


Diouf l’humaniste

Journaliste, agent, puis président d’un club réputé turbulent, presque ingérable, où il parvient à se faire apprécier par les exigeants supporters olympiens malgré l’absence de titre, Diouf reste fidèle à Marseille. La page OM refermée, il devient  actionnaire et formateur à l’European Communication School et de l’Institut européen de journalisme à Marseille, intervient pour l’Officiel des paris en ligne et est nommé chevalier de la Légion d’honneur par l’ancien président français Français Hollande.

En 2013, dans son livre C’est bien plus qu’un jeu (Éditions Grasset), Diouf avait critiqué la gestion de son successeur à l’OM, Vincent Labrune, entraînant une réaction virulente de club, l’accusant de « tentatives de manipulation. » Il devient aussi consultant pour Le Monde et Canal +. Son éloquence et sa franchise lui permettaient de livrer sans détour le fond de sa pensée, n’hésitant jamais à bousculer les instances internationales du football, dont la Confédération africaine de football (CAF) et la FIFA.

Presque sans surprise, Diouf, homme de gauche revendiqué, est sollicité par le Parti socialiste (PS) et Europe Écologie Les Verts (EELV) en vue des élections municipales françaises de 2014. Il prend finalement la tête de la liste « Changer la donne », constituée de personnalités hétéroclites, et qui obtient 5,33 %.

Son – long séjour – marseillais sera à peine terni en 2016 par sa mise en examen, vite transformée en statut de témoin assisté, dans l’affaire des transferts douteux de l’OM, assez pour l’inciter à passer l’essentiel de son temps à Dakar, avant de revenir plus régulièrement à Marseille. Là, cet humaniste – un terme souvent employé pour résumer sa personnalité –  pouvait constater à chacune de ces longues balades qu’il affectionnait tant  que sa popularité restait intacte auprès des Marseillais.

Jeune Afrique adresse à la  famille et aux proches de Pape Diouf ses plus sincères condoléances.        </p>
